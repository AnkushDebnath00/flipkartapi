import mongoose from "mongoose";

// Validation
const userSchema = new mongoose.Schema({
  firstname: {
    type: String,
    required: true,
    trim: true,
  },
  lastname: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
    max: 12,
    min: 5,
  },
});

const User = mongoose.model("user", userSchema);
export default User;
